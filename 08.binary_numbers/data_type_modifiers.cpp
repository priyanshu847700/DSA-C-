//data type modifiers
// 1. long 
// 2. short
// 3. signed
// 4. unsigned
// 5. long long


#include <iostream>
using namespace std;
int main(){
    cout<<sizeof(int)<<endl;            //4
    cout<<sizeof(long int)<<endl;       //8
    cout<<sizeof(long long int)<<endl;  //8
    cout<<sizeof(short int)<<endl;      //2
    cout<<sizeof(double)<<endl;         //8
    return 0;
}





